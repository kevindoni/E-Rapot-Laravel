<?php

namespace App\Http\Controllers;

use App\Models\Kelas;
use App\Models\KelasSiswa;
use App\Models\Siswa;
use Illuminate\Http\Request;

class KelasSiswaController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $kelas = Kelas::with('siswa')->orderBy('nama_kelas')->get();
        return view('admin.kelas.siswa.index', compact('kelas'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $kelas = Kelas::orderBy('nama_kelas')->get();
        $siswa = Siswa::where('lulus', 'Belum Lulus')->orderBy('nama_siswa')->get();
        return view('admin.kelas.siswa.create', compact('kelas', 'siswa'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'kelas' => 'required',
            'siswa' => 'required'
        ]);

        $kelas = Kelas::find($request->kelas);
        $kelas->siswa()->attach($request->siswa);

        return redirect()->route('kelas-siswa.index')->with('success', 'Kelas siswa berhasil disimpan');
    }

